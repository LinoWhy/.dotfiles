return {
  {
    "goolord/alpha-nvim",
    event = "VimEnter",
    keys = { { "<leader>;", "<cmd>Alpha<cr>", desc = "Dashboard" } },
    config = function()
      local alpha = require("alpha")
      local dashboard = require("alpha.themes.dashboard")
      local icons = Lino.icons

      math.randomseed(os.time())
      dashboard.section.header.val = headers[math.random(1, #headers)]
      dashboard.section.header.opts.hl = "Label"

      dashboard.section.buttons.val = {
        dashboard.button(
          "f",
          icons.ui.FindFile .. "  Find File",
          "<cmd>Telescope find_files find_command=rg,--ignore,--hidden,--files<cr>"
        ),
        dashboard.button("n", icons.ui.NewFile .. "  New File", "<cmd>ene!<cr>"),
        dashboard.button("r", icons.ui.History .. "  Recent files", "<cmd>Telescope oldfiles<cr>"),
        dashboard.button(
          "c",
          icons.ui.Gear .. "  Configuration",
          '<cmd>lua require("telescope.builtin").find_files({ cwd = vim.fn.stdpath("config") }) <cr>'
        ),
        dashboard.button("s", icons.ui.BookMark .. "  Restore Session", [[<cmd>Persisted load<cr>]]),
        dashboard.button("l", icons.ui.Sleep .. "  Lazy", "<cmd>Lazy<cr>"),
        dashboard.button("m", icons.ui.Package .. "  Mason", "<cmd>Mason<cr>"),
        dashboard.button("q", icons.ui.Quit .. "  Quit", "<cmd>quit<cr>"),
      }

      alpha.setup(dashboard.opts)

      vim.api.nvim_create_autocmd("UIEnter", {
        group = Utils.extra.augroup("alpha_once"),
        once = true,
        callback = function()
          local version = vim.version()
          local nvim_version_info = "󰔻 v" .. version.major .. "." .. version.minor .. "." .. version.patch
          local stats = require("lazy").stats()
          local startuptime = (math.floor(stats.startuptime * 100 + 0.5) / 100)
          dashboard.section.footer.val = nvim_version_info
            .. "    󰚥 "
            .. stats.loaded
            .. "/"
            .. stats.count
            .. "    󱑃 "
            .. startuptime
            .. "ms"
          pcall(vim.cmd.AlphaRedraw)
        end,
      })
    end,
  },
}
